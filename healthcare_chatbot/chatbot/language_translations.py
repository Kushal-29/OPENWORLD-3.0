def get_translation(language, key, **kwargs):
    """Get translated text for a key in the specified language"""
    if language not in TRANSLATIONS:
        language = 'english'  # Fallback to English
    
    text = TRANSLATIONS[language].get(key, TRANSLATIONS['english'].get(key, ''))
    
    # Replace placeholders like {name}, {symptoms}
    if kwargs:
        text = text.format(**kwargs)
    
    return text